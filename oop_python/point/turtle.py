# turtle graphics

import turtle

# Create a canvas instance
myturtle = turtle.Turtle()

myturtle.penup()
myturtle.goto(50,75)

myturtle.pendown()
myturtle.forward(100) # move 100 pixels
myturtle.forward(100) # Turn 90 degress left
myturtle.left(100)
myturtle.forward(200)

turtle.done()
